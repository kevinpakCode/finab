                                <h4>Grand Concert Live Festival : Le marché Tokp'Art enflammé par Kerozen et comapgnie</h4>
                                <p>

                                    <img src="img/kerozen1.jpg" />
                                    <p>Le marché Tokp'Art du FInAB installé à Fidjrossè plage fin pavés (Cotonou) a été le théâtre d'un spectacle mémorable le 18 Février 2023. En effet, la star ivoirienne Kerozen, le célèbre artiste N’Faly Konaté, la chanteuse FATY et plein d’autres talents de la musique béninoise et africaine ont offert un concert live sensationnel dans le cadre du Festival International des Arts du Bénin.</p>

                                    <p>Le public venu nombreux pour l'occasion a été conquis par la  grosse performance du chanteur qui a su créer une véritable ambiance de fête en interprétant ses plus grands titres à succès tels que "La Victoire", "Le Temps" ou encore "Mon Heure a Sonné".
</p>

                                    <p>L’artiste a également partagé la scène avec des artistes de renom tels que N’Faly Konaté, FATY, et bien d’autres offrant ainsi au public un spectacle riche en diversité musicale.
</p>

                                    <p>Le marché Tokp'Art est une innovation de cette première édition du FInAB. La magie de l'endroit alliée à la musique de Kerozen et de plein d'autres artistes a transporté le public dans un univers festif et chaleureux.
</p>

 

                                    
