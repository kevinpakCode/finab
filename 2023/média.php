                                <h4>Festival International des Arts du Bénin : La ville de Ouidah a accueilli une parade aux multiples couleurs</h4>
                                <p>

                                    <img src="img/ouidah.jpg" />
                                    <p>L’une des manifestations les plus remarquables de la première édition du Festival International des Arts du Bénin (FInAB) aura été la parade qui s’est déroulée dans la ville historique de Ouidah dont les rues ont été transformées en une scène animée où les festivaliers ont célébré la richesse du patrimoine culturel béninois et africain. </p>

                                    <p>En effet, le vendredi 17 Février 2023, les sons des tambours et instruments traditionnels ont résonné dans la cité Kpassè annonçant une fête très attendue dans cette ville. Ainsi, les masques Bourian importés par les descendants d'esclaves ont été présentés lors de cette belle parade qui a mobilisé du monde pour la circonstance. </p>

                                    <p>Les festivaliers ont dansé sur le rythme festif d'origine brésilienne, ramené par les Agoudas. Les masques Guèlèdè ont également été présentés. Faut-il le rappeler, ceux-ci sont inscrits au patrimoine immatériel de l'UNESCO et sont une source de fierté pour les communautés Yoruba et Nago du Bénin, du Nigeria et du Togo. 
</p>

                                    <p>Les tambours ont rythmé la parade alors que les acrobates du feu ont émerveillé les foules avec leurs performances dangereuses mais spectaculaires. Ils ont été particulièrement impressionnants, jonglant avec des éventails, des cordes et des bouts de bois en feu tout en faisant tourner des boules de feu autour d'eux. 
</p>

                                    <p>Des cracheurs de feu ont également captivé l'attention des spectateurs. Ils ont émerveillé le public par la précision et le danger de leur performance.
</p>

                                    <p>En somme, la Parade organisée à Ouidah dans le cadre du FInAB a été une belle expérience pour ceux qui y ont pris part. Elle a été une célébration de la richesse et de la diversité culturelle du terroir à travers plusieurs démonstrations  et performances.
</p>

                                    
