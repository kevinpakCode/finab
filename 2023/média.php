						<h4>Grand concert festival: Kérozen et plein d’artistes pour un incroyable spectacle à Cotonou</h4>
						<img src="img/kerozen.jpg" />
                                
                                <p>Le Festival International des Arts du Bénin célèbre l’art, la culture mais aussi la musique. Il offrira une série de concerts aux festivaliers avec plus d’une cinquantaine d’artistes invités pour des performances scéniques incroyables. C’est ainsi que Kérozen se produira sur scène à Cotonou le samedi 18 février 2023 à 18 heures à la faveur du grand concert live festival qui promet d'être un événement musical inoubliable. En effet, Kérozen est un artiste de renom dans le showbiz africain et est considéré comme l'un des meilleurs chanteurs de sa génération grâce à son style musical unique qui motive ses fans. Il est particulièrement attendu pour remotiver les élus de Cotonou. </p>

                                    <p>D'autres grands noms de la scène musicale seront au rendez-vous.</p>

                                    <p>Ce grand concert live festival sera également une occasion pour le public de communier avec d’autres talents de la musique béninoise et africaine. Ainsi, Faty, les Teriba, N'Fally Kouyaté,… seront sur scène pour des performances XXL. Ces artistes talentueux offriront au public une magnifique soirée aux multiples couleurs. Les tickets sont déjà en vente au prix de 2.000 FCFA pour les personnes qui souhaitent assister à ce grand concert live festival qui s’annonce exceptionnel. Alors, préparez-vous à passer une soirée de rêve en compagnie de ces valeurs sures de la musique africaine. Tous à Fidjrossè plage fin pavés pour vivre un concert très excitant.</p>
							
							 
                                
								<h4>FInAB : La professionnalisation de la mode en Afrique au cœur des échanges</h4>
								<img src="img/confo.jpeg" />
                                
                                <p>
                                    
L'industrie de la mode en Afrique connaît actuellement une croissance rapide et toutes les belles opportunités doivent être saisies pour discuter des enjeux liés à ce secteur. Le Festival International des Arts du Bénin y a pensé en s’associant à Oyemi Concept pour organiser une séance d’échange le 15 Février 2023 à 09H30 sur le marché Tokp’Art à Fidjrossè plage fin pavés sous le thème : la professionnalisation de la mode en Afrique.
Cette séance d'échange offrira une opportunité unique d'en discuter avec les acteurs les plus influents du secteur. Les participants pourront partager leurs expériences et leurs connaissances sur les défis actuels et les opportunités qui se présentent à eux.
ALPHADI est considéré comme l'un des pionniers de la mode africaine et son expérience sera précieuse pour les personnes intéressées par la professionnalisation de la mode en Afrique. Pepita D, est une créatrice de mode qui a su se faire un nom dans le monde de la mode au Bénin grâce à ses collections originales et audacieuses. Il en est de même pour Lolo Andoche et Edi Sessi reconnus dans le secteur de la mode. Leur contribution à la séance d'échange sera inestimable. Michelle Deguenon, fondatrice de l’Ecole de Stylisme, de Design et Accessoires de Mode apportera certainement une perspective unique sur les opportunités pour les jeunes créateurs de mode en Afrique.
Cette séance d'échange sera logiquement une occasion de rencontrer et d'interagir avec les leaders de l'industrie de la mode en Afrique et permettra aux participants d'acquérir des connaissances précieuses sur les tendances, les défis et les opportunités de la professionnalisation de la mode en Afrique.

                                </p>
								<h4>Afrogrill : Un festival pour valoriser le patrimoine culinaire béninois et africain.</h4>
								<img src="img/grill.jpg" />
                                
                                <p>
                                    Du 14 au 19 Février 2023 se tiendra à Cotonou, Ouidah et Porto-Novo le Festival International des Arts du Bénin organisé par le Groupe Empire. Un évènement qui vise à promouvoir le patrimoine artistique et culturel béninois à travers les arts, la musique, le théâtre, la danse, la littérature, la mode et le cinéma. Mais l’autre aspect du FInAB qui fera objet d’attraction est le festival Afrogrill. 

En effet, il est clair que la créativité sera au cœur de ce 1er Marché des arts du Bénin qui promet être riche en couleurs. Et au regard de la qualité du programme (Concert festival, concours de talents, expositions, défilé de mode, parade, conférences, visites touristiques, showcases, marché Tokp’Art, etc.) l’impatience est le seul sentiment qui anime la plupart des béninois et africains qui ne veulent point se faire conter l’évènement. 

Et qui parle de créativité parle aussi de la gastronomie africaine à laquelle le FInAB se fait le devoir d’attribuer sa place dans le monde de la culture par le biais d’Afrogrill, un festival de bouffe dans un festival. Ainsi, les amateurs de la cuisine locale et les fins gourmets auront le plaisir de déguster des plats locaux tels que Toubani, igname pilée, wassa-wassa, grillades, etc. Ils se régaleront aussi autour d’autres spécialités culinaires africaines : l’akoumé comme s’ils étaient au Togo, tanjia comme s’ils vivaient au Maroc, liboké sans voyager au Congo, yassa poulet sans prendre le  vol pour le Sénégal, attièkè sans traverser les frontières de la Côte d’Ivoire, etc.

Afrogrill vous dévoile toutes les bonnes saveurs made in Benin et made in Africa aux visiteurs et festivaliers présents à Cotonou (Fidjrossè Fin pavés).

Voici le programme du FInAB 2023

-Méga Concert festival 
-Festival gastronomique (AfroGrill )
-Marché Tokp'Art 
-Concours de talents artistiques (Slam, danse, magie, contorsion, music, mode, humour, arts plastiques,...)
-Défilé de mode 
-Animation de stands
-Expositions d’arts
-Visite touristique
-Conférences 
-Parade
- etc.
