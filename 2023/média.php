								  <h4>Festival International des Arts du Bénin : Des sommités au rendez-vous</h4>
                                <img src="img/cadresfinab.jpg" />
                              
                                <p>
                                    <p>La première édition du Festival International des Arts du Bénin (FInAB) aura lieu du 14 au 19 Février 2023 dans les villes de Cotonou, Ouidah et Porto-Novo. Durant six jours environ, la création contemporaine africaine sera à l’honneur pour mettre en lumière le savoir-faire des créateurs béninois et africains. S’il est vrai que les artistes africains se contentent généralement des marchés européens pour exposer et vendre leurs œuvres, le FInAB se fixe pour mission à moyen et long terme de changer la donne en déplaçant dorénavant des collectionneurs vers les créateurs afin de doter le Bénin et l’Afrique d’un vrai marché d’arts. Au regard de l’engouement autour de l’évènement, des ressources déployées et des personnalités et célébrités attendues, il sied de faire remarquer que l’espoir est permis et ça promet une très belle fête populaire. </p>

