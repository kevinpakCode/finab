								<h4>Première édition du FInAB : Le Ministre Oswald HOMEKY sonne le gong et lance les hostilités.</h4>
                                <p>

                                    <img src="img/lancement.jpg" />
                                    <p>Le Festival International des Arts du Bénin (FInAB) a été officiellement lancé le mardi 14 Février 2023 au marché Tokp’Art sis à Fidjrossè plage fin pavé à Cotonou devant un parterre de personnalités. Autorités politico-administratives, acteurs et figures majeures du monde des arts et de la culture, invités, festivaliers et curieux ont assisté à une belle cérémonie empreinte de solennité.</p>

                                    <p>Au nom de son homologue du ministère de la Culture Jean-Michel ABIMBOLA absent du territoire national, le ministre des sports Oswald HOMEKY a officiellement lancé les activités du 1er Marché des Arts du Bénin qui s’est déroulé du 14 au 19 Février 2023 à Cotonou, Ouidah et Porto-Novo. Et pour le compte de cette première édition du FInAB qui a braqué ses projecteurs sur les 7 arts au Bénin à savoir, la musique, la danse, le théâtre, le cinéma, l’art plastique, la littérature et la mode, tous les acteurs qui se sont succédés au pupitre ont salué la grandeur d’esprit et l’audace de son jeune promoteur Ulrich ADJOVI, PDG du Groupe Empire. « Nous sommes fiers de vous compter parmi les jeunes acteurs qui contribuent à la concrétisation de l’ambition du Président de la République. Nous sommes fiers de vous voir prendre autant de risques en organisant un festival de cette envergure en 9 mois. Il faut déjà avoir l’audace » dira le Ministre des sports Oswald Homeky.</p>

                                    <p>Plus qu’un festival, le FInAB est une belle opportunité de découverte du savoir-faire des artistes. Le festival met également en valeur la richesse gastronomique du terroir. La première autorité de la ville de Cotonou Luc ATROKPO s’en réjouit. « Le FInAB est un évènement qui fait la fierté des cotonois et cotonoises car, l’un des objectifs du festival est de faire rayonner la ville de Cotonou…».</p>

                                    <p>Le FInAB faut-il le rappeler, a pour objectif de célébrer et de promouvoir l’art dans toutes ses dimensions à travers une centaine d’activités. C’est un festival né d’une expérience vécue par son promoteur. « L’idée du FInAB m’est venue lors d’une visite du siège de l’UEMOA au Burkina Faso. Dans une discussion, les Burkinabé se sont mis à parler du FESPACO (Festival panafricain du cinéma et de la télévision de Ouagadougou), les Ivoiriens du FEMUA (Festival des Musiques Urbaines d'Anoumabo), mais au Bénin nous n’avions pas de festival de cette envergure. 9 mois après cette visite, nous accouchons du FinAB » confie Ulrich ADJOVI.</p>

                                    <p>La cérémonie a aussi été une célébration riche et diversifiée de l'art et de la culture avec des performances éblouissantes.</p>

                                    
                                </p>
                                    <h4>Cérémonie d’ouverture du FInAB 2022 : De grandes figures honorées</h4>
                                
                                    <img src="img/recompense.jpg" />
                                    <p>L’un des moments forts ayant marqué la cérémonie d’ouverture de la première édition du Festival International des Arts du Bénin est la distinction des acteurs et personnalités du monde des arts et de la culture. Ils ont été honorés par le festival en guise de reconnaissance pour leur contribution inestimable au développement du Bénin et de l’Afrique à travers leurs disciplines respectives. Ainsi, la diva Angélique KIDJO, le grand artiste peintre Ludovic FADAIRO, le grand musicien Nel OLIVER, la danseuse Rachelle AGBOSSOU, Aladé Koffi Adolphe, les maîtres de la scène Alougbine Dine et Tola koukoui, des figures majeures de la mode béninoise Pépit’Art D et Lolo Andoche, l’écrivain Forent COUAO-ZOTTI et autres, ont été tous honorés et célébrés à travers des trophées à eux décernés.</p>

                                    <p>Des distinctions spéciales sont également décernées aux artistes émérites du FInAB. En effet, le magicien du désert ALPHADI a reçu un prix spécial pour reconnaitre et saluer sa contribution à l’essor de la mode en Afrique. Claude BALOGOUN est un acteur clé et incontournable dans le cinéma béninois. Il a été honoré tout comme le grand maître du papier, le marocain Tibari Kantour. Les lauréats ont reçu leurs prix sous les ovations du public, témoignant ainsi de leur impact considérable sur l'art et la culture béninoise et africaine. </p>

                                    <p>Le Festival International des Arts du Bénin est une plateforme de choix pour la promotion de l'art et de la culture africaine. Cette cérémonie de distinction est donc une reconnaissance bien méritée pour les artistes dans leurs domaines respectifs.</p>
                                    
                                </p>
						
						<h4>Cérémonie d’ouverture du FInAB 2022 : Les tambours de Pepit’Arts ont résonné</h4>
                                <p>

                                    <img src="img/petitart.jpg" />
                                    <p>S’il y a un spectacle qui a marqué les esprits lors de la cérémonie d’ouverture de la première édition du Festival International des Arts du Bénin, c’est bien la belle prestation de Pepit’Arts, un groupe de danse patrimoniale, de percussion et de performance du Centre des Arts et Métiers de Médédjonou dans la commune d'Adjarra au Bénin. Pepit’Arts est composé d’enfants qui capitalise une riche expérience en matière de participation à des festivals tant en Afrique qu’en Europe. Venus de la cité des tambours, ces enfants ont su comme à leur habitude captiver les attentions et recevoir des ovations nourries pour leur grosse performance devant un parterre de personnalités.</p>

                                    <p>Chaque tambour a été utilisé avec une grande maîtrise, montrant la polyvalence et l'habileté du groupe à créer des rythmes variés, rapides et complexes. Un groupe qui a su transmettre sa passion et son talent pour la musique avec une intensité incroyable, démontrant que la percussion est une forme d'art à part entière. La cérémonie d’ouverture du FInAB a été très riche en couleurs et Pepit’Arts y a largement contribué en montrant son talent et sa passion pour la musique et en démontrant que la percussion est une forme d'art unique et étonnante.</p>

                                    
                                </p>
					
					<h4>Art exhibition Alphadi-Ouardane: Quand la mode rencontre l'art contemporain au FInAB</h4>
                                <p>

                                    <img src="img/alphadi.jpg" />
                                    <p>Les amateurs d’art et de la mode ont profité du beau spectacle à eux offert par deux artistes, l’un créateur de mode et l’autre peintre contemporain à l’occasion de la première édition du Festival International des Arts du Bénin organisé du 14 au 19 Février 2023 à Cotonou, Ouidah et Porto-Novo.</p>

                                    <p>En effet, Seidnaly Sidhamed alias Alphadi, fondateur du Festival international de la mode en Afrique et considéré comme icône de la mode africaine et l’artiste marocain Abderrahmane Ouardane ont fusionné leur talent et leur créativité pour offrir une exhibition exceptionnelle et inédite au public. A travers une exposition d'art organisé le 14 Février 2023 au marché Tokp'Art à Fidjrossè plage fin pavés, ces deux artistes de renom ont permis aux festivaliers spécialement les amateurs d'art et de mode de se rassembler et de découvrir des œuvres d'art ainsi que des créations exceptionnelles.</p>

                                    <p>On pouvait admirer avec bonheur un mélange de couleurs, de textures et de styles. Les vêtements exposés par des mannequins sont une interprétation contemporaine des tenues traditionnelles africaines avec des touches modernes et innovantes. Les œuvres d'art, quant à elles, sont des peintures et des photographies qui représentent l'identité africaine. « Nous avons travaillé ensemble dans le sens du rapprochement entre la mode et l’art contemporain » a déclaré avec fierté Abderrahmane Ouardane.</p>

                                    <p>Cette exhibition est une initiative de Magali Dossa, promotrice de Oyèmi Fashion Show, un évènement qui promeut et valorise la mode africaine et dont la récente édition a été honorée par la présence du Chef de l’Etat béninois, son Excellence Patrice TALON. «…Je suis honorée de voir l’art et la mode se rencontrer pour offrir quelque chose de spécial au public… » s’est réjouit Magali Dossa.</p>

                                    <p>Cette exhibition a eu le mérite de rassembler un public composé d'amateurs d'art, de stylistes et de designers, des personnalités du monde de l'art et autres pour découvrir et apprécier la rencontre entre l'art et la mode. Une rencontre qui aura été un véritable succès et qui aura aussi permis de mettre en valeur le talent de ces deux acteurs qui promeuvent l’art et la culture africaine à travers leurs créations.</p>

                                    
