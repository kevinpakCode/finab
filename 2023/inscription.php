                                                Le Festival International des Arts du Bénin (FInAB) est une initiative du GROUPE EMPIRE qui rassemblera tous les deux (2) ans, des passionnés d’art, les professionnels et les marchands d’art, toutes disciplines confondues en provenance du Bénin et de l’International. Le Festival abritera un marché d’art dénommé « TOKP’ART » inspiré du marché « Tokpa », le plus grand marché de l’Afrique de l’Ouest. Seront également présents sur le FInAB, les acteurs culturels, les programmateurs, les acheteurs de spectacles, les partenaires, les touristes, etc. de plusieurs pays. Outre les nombreux artistes talentueux venant de divers horizons, le FInAB à chaque édition donnera un coup de projecteur sur un pays africain. Le premier pays invité pour cette édition est le Maroc. Cette invitation vise à développer le rapprochement des deux pays et de leurs cultures. Le Festival International des Arts Du Bénin (FInAB) s’adresse à tout public. Son cœur de cible étant les passionnés d’arts et de culture friands de nouveautés, les artistes, les acteurs culturels du Bénin, de l’Afrique et du monde, les populations béninoises, les touristes, l’audimat des médias. 

