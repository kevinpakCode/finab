                                <p>Bienvenue au FInAB</p>
                                <h2>Contexte</h2>
                            </div>
                            <div class="about-text">
                                <p>
                                    Au Bénin, la promotion de la culture et des Arts a fait l’objet de plusieurs initiatives venant aussi bien de l’Etat béninois que des acteurs privés. Au nombre de ces dernières, nous distinguons entre autres le FITHEB, le FESNAC, le FIMuB du côté du secteur public puis ‘‘Cotonou Couleur Jazz’’, ‘‘Moov dance crew’’, ‘‘Yello Summer’’ du privé qui, malgré l’engouement qu’elles ont suscité, n’ont connu malheureusement que peu d’éditions. D’un secteur comme de l’autre, la cause de l’arrêt de ces initiatives est, pour la plupart, une mauvaise organisation ou le manque de financement. Avec la récente dissolution du FITHEB, cette Biennale qui faisait la fierté du pays à l’international, il ne nous reste à ce jour que le Festival SICA, le FIFF, le FIP, le festival MIA, qui n’embrassent que la musique et le cinéma. C’est au regard de ses constats que le FInAB a été créé pour doter le Bénin d’un festival d’envergure internationale embrassant toutes les disciplines artistiques.
                                </p>
