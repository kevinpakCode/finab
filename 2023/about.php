                                <p>Pourquoi le FInAB?</p>
                                <h2>Justification</h2>
                            </div>
                            <div class="about-text">
                                <p>
                                    L’Afrique et précisément le Bénin regorge de nombreux talents. Le tourisme, la culture et les arts sont des secteurs d’investissements porteurs. Leurs marchés se développent rapidement et de plus en plus de créateurs sont maintenant cotés sur le marché international. Cependant, beaucoup de ces créateurs manquent de visibilité et de moyens financiers pour développer convenablement leurs activités. Le FInAB a pour ambition de répondre à ces problématiques en assurant la promotion nationale et internationale des créateurs auprès des professionnels et particuliers (programmateurs, galeristes, collectionneurs...) de différents domaines, de plusieurs pays et leur exposition au grand public.
